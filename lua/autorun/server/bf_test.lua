local code = {
    "return coroutine.wrap(function(mem,write)",
        "local ptr=0",
        "local count=0",
        "local function yield() count=count+1 if count>100000 then count=0 coroutine.yield(false) end return true end",
        "local band=bit.band"}

local ptr_change = 0
local data_change = 0
for i=1,#input do
    local symbol = input[i]
    
    if data_change ~= 0 and symbol ~= "+" and symbol ~= "-" then
        table.insert(code,"mem[ptr+"..ptr_change.."]=mem[ptr+"..ptr_change.."]+"..data_change)
        data_change = 0
    end

    if ptr_change ~= 0 and (symbol == "[" or symbol == "]") then
        table.insert(code,"ptr=ptr+"..ptr_change)
        ptr_change = 0
    end
    
    if symbol == "+" then
        data_change = data_change+1
    elseif symbol == "-" then
        data_change = data_change-1
    elseif symbol == ">" then
        ptr_change = ptr_change+1
    elseif symbol == "<" then
        ptr_change = ptr_change-1
    elseif symbol == "[" then
        table.insert(code,"while mem[ptr]~=0 and yield() do")
    elseif symbol == "]" then
        table.insert(code,"end")
    elseif symbol == "." then
        table.insert(code,"write(mem[ptr+"..ptr_change.."]+"..data_change..")")
    end
end

table.insert(code,"return true")
table.insert(code,"end)")

print("==========>")

for k,v in pairs(code) do
    print(v)
end

print("==========>")

